WITH RankedColonies AS (
    SELECT 
        ID,
        SIZE_OF_COLONY,
        ROW_NUMBER() OVER (ORDER BY SIZE_OF_COLONY DESC) AS ROWNUM,
        COUNT(*) OVER () AS TOTAL_COUNT
    FROM 
        ECOLI_DATA
)

SELECT 
    ID,
    CASE 
        WHEN ROWNUM <= TOTAL_COUNT / 4 THEN 'CRITICAL'
        WHEN ROWNUM <= TOTAL_COUNT / 2 THEN 'HIGH'
        WHEN ROWNUM <= (TOTAL_COUNT * 3) / 4 THEN 'MEDIUM'
        ELSE 'LOW'
    END AS COLONY_NAME
FROM 
    RankedColonies
ORDER BY 
    ID;