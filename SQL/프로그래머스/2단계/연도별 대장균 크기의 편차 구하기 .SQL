WITH TMP1 AS (
    SELECT *, EXTRACT(YEAR FROM DIFFERENTIATION_DATE) AS YEAR
    FROM ECOLI_DATA
    )
    

SELECT A.YEAR, MAX_SIZE-TMP1.SIZE_OF_COLONY AS YEAR_DEV, TMP1.ID
FROM TMP1
JOIN (SELECT YEAR, MAX(SIZE_OF_COLONY) AS MAX_SIZE 
      FROM TMP1 
      GROUP BY YEAR) AS A ON A.YEAR = TMP1.YEAR
ORDER BY YEAR, YEAR_DEV
;